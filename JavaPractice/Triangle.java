public class Triangle {
    public static void main(String[] args) {
        System.out.print(" Lets create a triangle");

        int row = 0;

         while(row <= 5  )
         {

            int i =0;
            while (i<=row)
            {
                System.out.print(" *");
                i++;
            }
            System.out.println();
            row++;

         }

         int row1 = 4;

         while(row1 >= 0)

         { 
             int count =0;
            while (count <= row1)
            {
                System.out.print(" *");
                count ++;
            }
            System.out.println();
            row1--;

         }

        }

    }
    


    
    